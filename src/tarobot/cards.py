def build_deck() -> List[TarotCard]:
    deck: List[TarotCard] = []

    for name, upright, reversed_meaning in MAJOR_ARCANA:
        deck.append(
            TarotCard(
                name=name,
                arcana="major",
                upright_meaning=upright,
                reversed_meaning=reversed_meaning,
            )
        )

    for suit, suit_meanings in MINOR_SUITS.items():
        for rank, rank_meanings in MINOR_RANKS.items():
            deck.append(
                TarotCard(
                    name=f"{rank} {suit_meanings['card_name']}",
                    arcana="minor",
                    suit=suit,
                    rank=rank,
                    upright_meaning=f"{rank_meanings[0]}; тема карты: {suit_meanings['upright']}",
                    reversed_meaning=f"{rank_meanings[1]}; тема карты: {suit_meanings['reversed']}",
                )
            )

    return deck


def spread_labels(cards_count: int) -> List[str]:
    if cards_count == 3:
        return THREE_CARD_POSITIONS
    if cards_count == 5:
        return FIVE_CARD_POSITIONS
    return [f"Позиция {index}" for index in range(1, cards_count + 1)]


def draw_cards(cards_count: int, seed: Optional[int] = None) -> List[DrawnCard]:
    if cards_count <= 0:
        raise ValueError("cards_count must be positive")

    deck = build_deck()
    if cards_count > len(deck):
        raise ValueError(f"cards_count must be <= {len(deck)}")

    rng = random.Random(seed)
    labels = spread_labels(cards_count)
    selected = rng.sample(deck, cards_count)

    return [
        DrawnCard(
            position=index,
            position_label=labels[index - 1],
            card=card,
            orientation="reversed" if rng.random() < 0.3 else "upright",
        )
        for index, card in enumerate(selected, start=1)
    ]


def dominant_suit(cards: Iterable[DrawnCard]) -> Optional[str]:
    suits = [card.card.suit for card in cards if card.card.suit]
    if not suits:
        return None
    suit, _ = Counter(suits).most_common(1)[0]
    return suit


def major_arcana_count(cards: Iterable[DrawnCard]) -> int:
    return sum(1 for card in cards if card.card.arcana == "major")
