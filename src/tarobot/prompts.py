def build_yandex_reading_user_prompt(request: ReadingRequest, cards: Iterable[DrawnCard]) -> str:
    card_list = list(cards)
    lines: List[str] = [
        f"Вопрос пользователя: {request.question}",
        f"Количество карт: {request.cards_count}",
        "",
        "Контекст расклада:",
        *_spread_context_lines(card_list),
        "",
        "Режим ответа:",
        "- Нужен расширенный демонстрационный текст для озвучки, а не краткая трактовка.",
        "- Минимальный ожидаемый масштаб: reading.txt должен ощущаться как цельный рассказ, а speech — как 90-150 секунд живой речи.",
        "- Короткий ответ на 1-2 предложения в summary, card_sections или advice считается ошибкой.",
        "- Не пересказывай базовые значения карт дословно: используй их как сырье для истории вопроса.",
        "- Если вопрос пользователя похож на жизненную историю, сначала выдели его реальные ставки: безопасность, риск, надежду, страх ошибки, уже накопленное понимание.",
        "",
        "Карты в раскладе:",
    ]

    for card in card_list:
        orientation = "прямое положение" if card.orientation == "upright" else "перевернутое положение"
        card_type = _card_type_hint(card)
        lines.append(
            (
                f"{card.position}. {card.position_label} — {card.card.name}, {orientation}. "
                f"Тип: {card_type}. "
                f"Драматическая роль позиции: {_position_story_role(card.position_label)}. "
                f"Сюжетный фокус: {_scene_hint(card)}. "
                f"Состояние энергии: {_orientation_story_hint(card)}. "
                f"Базовое значение: {card.meaning}."
            )
        )

    lines.extend(
        [
            "",
            "Задача:",
            "- Сначала дай цельное толкование расклада.",
            "- Затем дай отдельные card_sections по каждой карте.",
            "- Потом дай advice.",
            "- В блоке speech сохрани ту же историю, но сделай ее ритмичной и пригодной для TTS.",
            "- Не превращай гадание в сухую инструкцию; нужен теплый, уверенный, немного мистический тон, но без навязчивой механики.",
            "- Используй вопрос пользователя как живой контекст: не отвечай абстрактно, связывай карты с формулировкой вопроса.",
            "- Финал speech должен повторять конкретный следующий шаг из advice, а не заменять его общей поддерживающей фразой.",
        ]
    )
    return "\n".join(lines)


def _spread_context_lines(cards: List[DrawnCard]) -> List[str]:
    if not cards:
        return ["- Карты не переданы; попроси корректный расклад."]

    lines = [
        f"- Сюжетная дуга: {_spread_arc_hint(cards)}",
        f"- Общий рисунок: {_spread_texture_hint(cards)}",
        "- Атмосфера: безымянный голос оракула говорит спокойно и собранно; допустим легкий налет стимпанка или киберпанка, но он не должен перекрывать человеческую историю.",
    ]

    suit = dominant_suit(cards)
    if suit:
        lines.append(f"- Доминирующая масть: {suit}; {_SUIT_STORY_HINTS.get(suit, 'используй масть как повторяющийся мотив')}.")
    else:
        lines.append("- Доминирующей масти нет: старшие арканы важнее бытовой плоскости, делай акцент на крупном внутреннем повороте.")

    major_count = major_arcana_count(cards)
    if major_count:
        lines.append(f"- Старшие арканы: {major_count} из {len(cards)}; это усиливает ощущение важного сюжетного узла.")
    else:
        lines.append("- Старших арканов нет: трактуй расклад как практический выбор, привычки и ближайшие решения.")

    reversed_count = sum(1 for card in cards if card.orientation == "reversed")
    if reversed_count:
        lines.append(
            f"- Перевернутые карты: {reversed_count} из {len(cards)}; показывай не катастрофу, а сомнение, задержку, перекос или энергию, которую нужно аккуратно развернуть."
        )
    else:
        lines.append("- Все карты прямые: ситуация читается открыто, но все равно ищи нюанс, а не плоское обещание успеха.")

    return lines


def _spread_arc_hint(cards: List[DrawnCard]) -> str:
    labels = [card.position_label for card in cards]
    if len(cards) == 3 and labels == ["Прошлое", "Настоящее", "Ближайшее будущее"]:
        return "три акта — что уже привело человека к развилке, где сейчас главный внутренний конфликт, какой ближайший ход вероятен"
    if len(cards) == 5 and labels == ["Суть вопроса", "Что помогает", "Что мешает", "Совет", "К чему идет"]:
        return "пять узлов — суть выбора, доступная опора, препятствие, совет и вероятный выход"
    return "читай позиции как последовательные части одной жизненной истории, а не как независимые карточные заметки"


def _spread_texture_hint(cards: List[DrawnCard]) -> str:
    reversed_count = sum(1 for card in cards if card.orientation == "reversed")
    major_count = major_arcana_count(cards)

    if major_count >= max(2, len(cards) // 2):
        scale = "крупный, судьбообразующий слой"
    elif major_count:
        scale = "один важный внутренний поворот внутри практической ситуации"
    else:
        scale = "практическая история ближайшего выбора"

    if reversed_count >= max(2, len(cards) // 2):
        tension = "с заметным сопротивлением и внутренней осторожностью"
    elif reversed_count:
        tension = "с одним или несколькими местами, где решение нужно сделать аккуратнее"
    else:
        tension = "с достаточно открытым ходом, где важнее точная настройка, чем борьба"

    return f"{scale}, {tension}"


def _card_type_hint(card: DrawnCard) -> str:
    if card.card.arcana == "major":
        return "старший аркан, крупный смысловой поворот"

    details = ["младший аркан"]
    if card.card.suit:
        details.append(f"масть {card.card.suit}")
    if card.card.rank:
        details.append(f"ранг {card.card.rank}")
    return ", ".join(details)


def _position_story_role(label: str) -> str:
    return _POSITION_STORY_ROLES.get(label, "отдельный поворот внутри общей истории вопроса")


def _scene_hint(card: DrawnCard) -> str:
    base = _SCENE_HINTS.get(
        card.position_label,
        "покажи, как эта позиция меняет понимание вопроса",
    )
    if card.orientation == "upright":
        return f"{base}; карта говорит через доступный ресурс или ясное проявление темы"
    return f"{base}; карта говорит через сомнение, задержку, страх или искажение темы"


def _orientation_story_hint(card: DrawnCard) -> str:
    if card.orientation == "upright":
        return "тема карты проявлена прямо; ее можно использовать как доступный ресурс"
    return "тема карты не исчезла, но идет через перекос, задержку или заедание; покажи, что именно требует настройки"


def _tone_hint(role: str, pitch_shift: float) -> str:
    lowered = pitch_shift < -20
    raised = pitch_shift > 20

    if role == "friendly":
        base = "теплая, мягкая, доверительная, без суеты"
    elif role == "strict":
        base = "собранная, ясная, спокойная, с внутренней опорой"
    elif role == "whisper":
        base = "очень интимная, тихая, почти шепотом, но без хоррора"
    else:
        base = "спокойная, естественная, живая, без механического тона"

    if lowered:
        return f"{base}; голос ощущается чуть ниже обычного"
    if raised:
        return f"{base}; голос ощущается чуть выше обычного"
    return base
